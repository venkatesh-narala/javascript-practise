var marks=prompt("Enter your marks")

if(marks>=90){
    document.write("you got a A grade")
}
else if (marks>=80){
    document.write("you got a B grade")
}
else if (marks>=70){
    document.write("you got a C grade")
}
else if (marks>=60){
    document.write("you got a D grade")
}
else if (marks<=59){
    document.write("you got a F grade")
}

